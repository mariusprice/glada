## Installation Instructions:

1. **Download the theme files** using the download button
2. **Create a ZIP file** containing all the theme files
3. **Upload to WordPress**:
   - Go to your WordPress admin dashboard
   - Navigate to Appearance → Themes
   - Click "Add New" → "Upload Theme"
   - Upload your ZIP file
   - Activate the theme

4. **Set up the home page**:
   - Go to Pages → Add New
   - Create a new page titled "Home"
   - In the Page Attributes box, select "Home Page Template" from the Template dropdown
   - Publish the page
   - Go to Settings → Reading
   - Set "Your homepage displays" to "A static page"
   - Select your "Home" page as the homepage

